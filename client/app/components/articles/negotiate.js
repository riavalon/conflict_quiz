import React, {Component} from 'react';


export class Negotiate extends Component {
	constructor(props) {
		super(props);
	}

	render() {
		return (
			<div>
				<div class="about-type">
					<h1 class="caps mb1 bold">
						Bargaining or Negotiation
					</h1>

					<p>Bargaining and negotiation can be used interchangeably; they mean virtually the same thing. Bargaining or Negotation refer to a strategy that is very similar to Collaboration, except that the expectations of the parties are lower as they enter the conflict arena. Instead of seeking solutions that are mutually fulfilling to both or all parties, people who use Negotiation are trying to get as much as they can, assuming that they will not get everything they want (but at least they will get some of what they want, as the others get some of what they want). In other words, where Collaboration is a win-win strategy, Negotiation is a sorta-win-sorta-lose strategy.</p>
				</div>

				<hr />

				<div class="when-to-use">
					<h2 class="caps mb1 bold">When to use Bargaining</h2>

					<p>Negotiation can be used at all levels. People are used to it, they expect it, and they usually know how to deal with it. A frequent problem with the use of Negotiation, however, is the fact that many issues or problems are not negotiable.</p>

					<p>To bargain, the "prize" must be something that is divisible or items one can trade. Negotation is often a good compromise between Collaboration and Compelling. It is necessary for all the parties to be willing to bargain. All parties must come to the table and join in the give and take.</p>

					<p>
						Bargaining is not a good idea in situations where there is a great power disparity between parties (unless the power disparity is compensated in some way). What may officially be designated as bargaining does not feel like bargaining when one of the parties perceives that they must go along with the proposals suggested by the other. (This is not Negotiating; it is Compelling.)
					</p>

					<p>Finally, bargaining is not a good strategy to use when any of the parties has a high level of fear and perceptual distortion about the other and the situation. This is a condition in which Negotiation is not possible but in which it is best postponed until all sides feel a modicum of safety and each is able to approach the problems somewhat rationally and with a clear head.</p>

				</div>
			</div>
		);
	}
}
